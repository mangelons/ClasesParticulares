    private void comboAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboAlumnoActionPerformed

    if(comboAlumno.getSelectedIndex()>=0)
    {
        Alumno a = alumnos.get(comboAlumno.getSelectedIndex());
        DefaultListModel pendientes = new DefaultListModel();
        clases = new ArrayList<>();
        for(Clase c : a.getClases())
        {
            if(!c.getPagada())
            {
                pendientes.addElement(c);
                clases.add(c);
            }
        }
        botonFormalizar.setEnabled(false);
        listaPendientes.setModel(pendientes);
        listaPendientes.setSelectedIndex(0);
    }
    }//GEN-LAST:event_comboAlumnoActionPerformed

    private void botonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirActionPerformed
        DefaultListModel mPendientes = (DefaultListModel) listaPendientes.getModel();
        DefaultListModel mPagados = (DefaultListModel) listaPagar.getModel();
        int indice =listaPendientes.getSelectedIndex();
        mPagados.addElement(mPendientes.get(indice));
        mPendientes.remove(indice);
        if(!mPagados.isEmpty())
        {
            botonFormalizar.setEnabled(true);
        }
        else
        {
            botonFormalizar.setEnabled(false);
        }
        
        listaPendientes.setSelectedIndex(0);
    }//GEN-LAST:event_botonAnadirActionPerformed

    private void botonQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonQuitarActionPerformed
        DefaultListModel mPendientes = (DefaultListModel) listaPendientes.getModel();
        DefaultListModel mPagados = (DefaultListModel) listaPagar.getModel();
        int indice =listaPagar.getSelectedIndex();
        mPagados.remove(indice);
        mPendientes.addElement(clases.get(indice));
        
        if(!mPagados.isEmpty())
        {
            botonFormalizar.setEnabled(true);
        }
        else
        {
            botonFormalizar.setEnabled(false);
        }
    }//GEN-LAST:event_botonQuitarActionPerformed

