    private void botonFormalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonFormalizarActionPerformed
        DefaultListModel mPagados = (DefaultListModel) listaPagar.getModel();
        int numClases = 0;
        int pagados = mPagados.size();
        for(int i = 0; i < pagados; i++)
        {
            for(int j = 0; j < clases.size(); j++)
            {
                Clase aux = (Clase) mPagados.get(i);
                if(aux.equals(clases.get(j)))
                {
                    clases.get(j).pagarClase();
                    numClases+=clases.get(j).getHoras();
                }
            }
        }
        
        Alumno a = alumnos.get(comboAlumno.getSelectedIndex());
        double precio = a.getPrecio();
        JOptionPane.showMessageDialog(null, "Precio a pagar: "+(precio*numClases)+" €", "Precio a pagar: clases "+a.getNombre(), JOptionPane.INFORMATION_MESSAGE);
        
        new Vista(alumnos).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_botonFormalizarActionPerformed

