    private void botonJustificanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonJustificanteActionPerformed
        try {
            String nombre = comboAlumnosResumen.getItemAt(comboAlumnosResumen.getSelectedIndex());
            FileWriter f = new FileWriter("Resumen_"+nombre+".txt");
            int indice = comboAlumnosResumen.getSelectedIndex();
            Alumno a = alumnos.get(indice);
            StringBuilder pagada = new StringBuilder();
            StringBuilder porpagar= new StringBuilder();
            for(Clase c : a.getClases())
            {
                if(c.getPagada())
                    pagada.append(c).append("\n");
                else
                    porpagar.append(c).append("\n");
            }
            StringBuilder resumen = new StringBuilder();
            resumen.append("RESUMEN DE CLASES -- MIGUEL ÁNGEL TORRES FONT\n");
            resumen.append("Herramienta de Gestión ClasesParticulares desarrollada por Miguel Ángel Torres Font");
            resumen.append("\nVersion 1.0\n");
            resumen.append("Alumno: "+a.getNombre()+" "+a.getApellidos());
            resumen.append("\n\n\n");
            resumen.append("PENDIENTES DE PAGO\n");
            resumen.append(porpagar.toString());
            resumen.append("\nPAGADAS\n");
            resumen.append(pagada.toString());
            f.write(resumen.toString());
            f.close();
            
        } catch (IOException ex) {
            Logger.getLogger(Vista_Resumen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonJustificanteActionPerformed

