    private void comboAlumnosResumenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboAlumnosResumenActionPerformed
        DefaultListModel pagada = new DefaultListModel<>();
        DefaultListModel porpagar = new DefaultListModel<>();
        int indice = comboAlumnosResumen.getSelectedIndex();
        Alumno a = alumnos.get(indice);
        
        for(Clase c : a.getClases())
        {
            if(c.getPagada())
            {
                pagada.addElement(c);
            }
            else
                porpagar.addElement(c);
        }
        
        listaPagadas.setModel(pagada);
        listaPendiente.setModel(porpagar);
    }//GEN-LAST:event_comboAlumnosResumenActionPerformed

